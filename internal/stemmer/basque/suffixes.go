func findSuffix(w *snowballword.SnowballWord, actions []suffixAction) (suffixAction, bool) {
	for _, sa := range actions {
		if w.HasSuffixRunes(sa.suffixRunes) {
			return sa, true
		}
	}
	return suffixAction{}, false
}
